inline static std::size_t get_hash(uint64_t board, int color) {
  std::size_t result = 0;
  const auto board_ptr = reinterpret_cast<const uint8_t*>(&board);
  auto base_index = color * 256 * 8;

  for (auto i = 0; i < 8; ++i) {
    result ^= hash_array[base_index + board_ptr[i]];
    base_index += 256;
  }

  return result;
}  // namespace
